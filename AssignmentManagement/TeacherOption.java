    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        StringBuilder studentData = new StringBuilder();

        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");

            Connection con = DriverManager.getConnection("jdbc:ucanaccess://studentinfo1.accdb");

            int TeacherEnroll = Enrollment;

            String sql = "SELECT * FROM SC WHERE CourseTeacherEnroll = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, TeacherEnroll);

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String studentEnroll = rs.getString("StudentEnroll");
                String studentName = rs.getString("StudentName");
                String courseName = rs.getString("CourseName");
                String courseCode = rs.getString("CourseCode");
                String marksObtained = rs.getString("MarksObtained");

                studentData.append("Student Enrollment: ").append(studentEnroll).append("\n");
                studentData.append("Student Name: ").append(studentName).append("\n");
                studentData.append("Course Name: ").append(courseName).append("\n");
                studentData.append("Course Code: ").append(courseCode).append("\n");
                studentData.append("Marks Obtained: ").append(marksObtained).append("\n\n");
                studentData.append("-------------------------------------------").append("\n\n");
            }

            if (studentData.length() == 0) {
                studentData.append("No Student Enrolled").append(TeacherEnroll).append("\n");
            }

            rs.close();
            pst.close();
            con.close();
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        } catch (SQLException e) {
            e.printStackTrace();
        }

        // Use the studentData StringBuilder as needed
        // Example: display the data in a label
        txtOutput.setText(studentData.toString());

    }//GEN-LAST:event_jButton2ActionPerformed

