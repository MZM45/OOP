    private void txtLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLoginActionPerformed
        int studentEnrollment = Integer.parseInt(txtStudentEnrollment.getText());
int marksObtained = Integer.parseInt(txtStudentObtainedMarks.getText());

try {
    Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
    Connection con = DriverManager.getConnection("jdbc:ucanaccess://studentinfo1.accdb");



    // Check if both teacher enrollment and student enrollment exist in the SC table
    String searchSql = "SELECT * FROM SC WHERE StudentEnroll = ? AND CourseTeacherEnroll = ?";
    PreparedStatement searchPst = con.prepareStatement(searchSql);
    searchPst.setInt(1, studentEnrollment);
    searchPst.setInt(2, Enrollment);
    ResultSet searchRs = searchPst.executeQuery();

    if (searchRs.next()) {
        // Student and teacher enrollments found, update the marks obtained
        String updateSql = "UPDATE SC SET MarksObtained = ? WHERE StudentEnroll = ? AND CourseTeacherEnroll = ?";
        PreparedStatement updatePst = con.prepareStatement(updateSql);
        updatePst.setInt(1, marksObtained);
        updatePst.setInt(2, studentEnrollment);
        updatePst.setInt(3, Enrollment);

        int rowsAffected = updatePst.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Marks updated successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to update marks");
        }

        updatePst.close();
    } else {
        // Student and/or teacher enrollment not found
        JOptionPane.showMessageDialog(null, "Student and/or teacher enrollment not found in SC table");
    }

    searchRs.close();
    searchPst.close();
    con.close();
} catch (Exception e) {
    System.out.println(e);
}
        
        
    }//GEN-LAST:event_txtLoginActionPerformed

