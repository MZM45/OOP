    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String TeacherName = txtTeacherName.getText();
        String TeacherDepartment = txtTeacherDepartment.getText();
        int TeacherEnrollment = Integer.parseInt(txtTeacherEnrollment.getText());
        int TeacherExperience = Integer.parseInt(txtTeacherExperience.getText());
        String TeacherCourseCode = txtTeacherCourseCode.getText();
        String TeacherSpecialization = txtTeacherSpecialization.getText();
        String TeacherPassword = txtTeacherPassword.getText();

//        Menu menuobj = new Menu(StudentName, StudentAge, StudentAddress, StudentCnic, StudentEnrollment, StudentSection, StudentSemester);
      Faculty FO = new Faculty(TeacherName,TeacherCourseCode,TeacherEnrollment,TeacherDepartment,TeacherSpecialization,TeacherExperience);
        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            Connection con = DriverManager.getConnection("jdbc:ucanaccess://studentinfo1.accdb");
            String sql = "INSERT INTO TD( Name,Enrollment, Department,Specialization,YearsOfExperience,TeacherCourseCode,Password) VALUES (?,?,?,?,?,?,?)";
           PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, FO.getName());
          pst.setString(3, FO.getDepartment());

            pst.setInt(2,FO.getEnrollment());
            pst.setString(4, FO.getSpecialization());

            pst.setInt(5, FO.getYearsOfExperience());
            pst.setString(6, FO.getTeacherCourseCode());

            pst.setString(7,TeacherPassword);
            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, " Teacher added succesfully");
            } else {
                JOptionPane.showMessageDialog(null, " failed");
            }
            pst.close();
            con.close();
        } catch (Exception e) {
            System.out.println(e);
      }  

    }//GEN-LAST:event_jButton2ActionPerformed

